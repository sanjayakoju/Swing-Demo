    private void jTextField_productIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField_productIdActionPerformed
        // TODO add your handling code here:
        UserTrace ut=new UserTrace();
        String str=jTextField_productId.getText();
        place = jTextField_place.getText();
        int qty =Integer.parseInt(jTextField_dqty.getText());
        qty=Math.abs(qty);
        double total;
        if(!str.equals(""))
        {
            int pid=Integer.parseInt(str);
            ProductController pdc=new ProductController();
            Product ob=pdc.fetchData(pid);
            if(!(pid >='0' && pid <='9'))
            {
                //evt.consume();
            }
            if(ob!=null)
            {
                total=qty*ob.getPrice();
                model.addRow(new Object[]{ob.getId(),ob.getNames(),qty,ob.getPrice(),total,new Date().toGMTString(),ut.getUsername(),place});
            }
            
            grandTotal();
        }
        jTextField_productId.setText("");
        jTextField_place.setText("");
        jTextField_dqty.setText("");
    }//GEN-LAST:event_jTextField_productIdActionPerformed

