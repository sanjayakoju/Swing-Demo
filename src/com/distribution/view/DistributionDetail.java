    private void jButton_saveAndPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_saveAndPrintActionPerformed
        // TODO add your handling code here:
         ArrayList<Distribution> list=new ArrayList();
         int count=jTable_distribution.getRowCount();
         
         for(int i=0;i<count;i++)
        {
            Distribution ob=new Distribution();
            int did=UserTrace.id;
            int pid=Integer.parseInt(jTable_distribution.getValueAt(i,0).toString());
            String names=jTable_distribution.getValueAt(i,1).toString();
            
            int qty=Integer.parseInt(jTable_distribution.getValueAt(i,2).toString());
            int price=(int) Double.parseDouble(jTable_distribution.getValueAt(i,3).toString());
            double total=Double.parseDouble(jTable_distribution.getValueAt(i,4).toString());
            java.util.Date d=new java.util.Date();
            //String d=new java.util.Date().toString();
            String checkedBy=(jTable_distribution.getValueAt(i,6).toString());
            place=(jTable_distribution.getValueAt(i,7).toString());
            
            ob.setPid(pid);
            ob.setDid(did);
            ob.setNames(names);
            ob.setPrice(price);
            ob.setQty(qty);
            ob.setDate(d);
            ob.setCheckedBy(checkedBy);
            ob.setPlace(place);
            ob.setTotal(total);
            
            list.add(ob);
             
        }
        DistributionController sdc=new DistributionController();
        sdc.saveData(list);
        //JOptionPane.showMessageDialog(null,"Record Saved");
       printBill();

    
    }//GEN-LAST:event_jButton_saveAndPrintActionPerformed

